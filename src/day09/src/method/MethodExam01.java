package method;

public class MethodExam01 {

	public static void main(String[] args) {
		//# 1. 1~5까지의 합
		int sum = 0;
		for(int i = 1; i<=5; i++) {
			sum += i;}
		System.out.println(sum);
	
		
		sum = 0;
		

		//# 2. 1~10까지의 합
		for(int i = 1; i<=10; i++) {
			sum += i;}
		System.out.println(sum);
		sum = 0;
		
		
		//# 3. 5~100까지의 합
		for(int i = 50; i<=100; i++) {
			sum += i;}
		System.out.println(sum);
		sum = 0;
		
		
		//# 4. 35~75까지의 합
		for(int i = 35; i<=75; i++) {
			sum += i;}
		System.out.println(sum);
		sum = 0;
		
		
	}

}
